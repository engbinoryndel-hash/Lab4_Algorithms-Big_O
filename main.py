# Step 1: Implement binary search [cite: 82]
def binary_search(arr, target):  # [cite: 83]
    low = 0  # [cite: 84]
    high = len(arr) - 1  # [cite: 86]

    while low <= high:  # [cite: 87]
        mid = (low + high) // 2  # [cite: 89]
        if arr[mid] == target:  # [cite: 90]
            return mid  # [cite: 91]
        elif arr[mid] < target:  # [cite: 92]
            low = mid + 1  # [cite: 94]
        else:  # [cite: 93]
            high = mid - 1  # [cite: 95]
    return -1  # [cite: 96]

