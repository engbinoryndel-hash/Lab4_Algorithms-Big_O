# Step 1: Implement bubble sort [cite: 105]
def bubble_sort(arr):  # [cite: 106]
    n = len(arr)  # [cite: 107]
    for i in range(n):  # [cite: 108]
        for j in range(0, n - i - 1):  # [cite: 109, 167]
            if arr[j] > arr[j + 1]:  # [cite: 110]
                arr[j], arr[j + 1] = arr[j + 1], arr[j]  # [cite: 112]

