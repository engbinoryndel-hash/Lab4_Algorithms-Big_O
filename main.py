# Step 1: Implement selection sort [cite: 123]
def selection_sort(arr):  # [cite: 123]
    for i in range(len(arr)):  # [cite: 124]
        min_idx = i  # [cite: 124]
        for j in range(i + 1, len(arr)):  # [cite: 125, 126]
            if arr[j] < arr[min_idx]:  # [cite: 127]
                min_idx = j  # [cite: 128]
        arr[i], arr[min_idx] = arr[min_idx], arr[i]  # [cite: 129]

