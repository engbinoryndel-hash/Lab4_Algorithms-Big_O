# Step 1: Implement insertion sort [cite: 137]
def insertion_sort(arr):  # [cite: 138]
    for i in range(1, len(arr)):  # [cite: 139]
        key = arr[i]  # [cite: 140]
        j = i - 1  # [cite: 141]
        while j >= 0 and key < arr[j]:  # [cite: 142, 143]
            arr[j + 1] = arr[j]  # [cite: 144, 145]
            j -= 1  # [cite: 146]
        arr[j + 1] = key  # [cite: 147]

